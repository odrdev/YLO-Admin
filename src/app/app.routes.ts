/* eslint-disable @typescript-eslint/explicit-function-return-type */
export const appRoutes: Route[] = [

    // Redirect empty path to '/example'
    {path: '', pathMatch : 'full', redirectTo: 'folder'},

    // Redirect signed-in user to the '/example'
    //
    // After the user signs in, the sign-in page will redirect the user to the 'signed-in-redirect'
    // path. Below is another redirection for that path to redirect the user to the desired
    // location. This is a small convenience to keep all main routes together here on this file.
    {path: 'signed-in-redirect', pathMatch : 'full', redirectTo: 'folder'},

    // Auth routes for guests
    {
        path: '',
        canActivate: [NoAuthGuard],
        canActivateChild: [NoAuthGuard],
        component: LayoutComponent,
        data: {
            layout: 'empty'
        },
        children: [
            {path: 'confirmation-required', loadChildren: () => import('app/modules/auth/confirmation-required/confirmation-required.routes')},
            {path: 'forgot-password', loadChildren: () => import('app/modules/auth/forgot-password/forgot-password.routes')},
            {path: 'reset-password', loadChildren: () => import('app/modules/auth/reset-password/reset-password.routes')},
            {path: 'sign-in', loadChildren: () => import('app/modules/auth/sign-in/sign-in.routes')},
        ]
    },

    // Auth routes for authenticated users
    {
        path: '',
        canActivate: [AuthGuard],
        canActivateChild: [AuthGuard],
        component: LayoutComponent,
        data: {
            layout: 'empty'
        },
        children: [
            {path: 'sign-out', loadChildren: () => import('app/modules/auth/sign-out/sign-out.routes')},
        ]
    },

    // Landing routes
    {
        path: '',
        component: LayoutComponent,
        data: {
            layout: 'empty'
        },
        children: [
            {path: 'home', loadChildren: () => import('app/modules/landing/home/home.routes')},
            {path: 'noaccess', loadChildren: () => import('app/modules/landing/noaccess/noaccess.routes')},
        ]
    },

    // Admin routes
    {
        path: '',
        canActivate: [AuthGuard],
        canActivateChild: [AuthGuard],
        component: LayoutComponent,
        resolve: {
            initialData: initialDataResolver
        },
        children: [
            {path: 'folder', loadChildren: () => import('app/modules/admin/folder/folder.routes')},
            {path: 'law', loadChildren: () => import('app/modules/admin/law/law.routes')},
            {path: 'article', loadChildren: () => import('app/modules/admin/article/article.routes')},
            {path: 'section', loadChildren: () => import('app/modules/admin/section/section.routes')},
            {path: 'topic', loadChildren: () => import('app/modules/admin/topic/topic.routes')},
            {path: 'doctrine', loadChildren: () => import('app/modules/admin/doctrine/doctrine.routes')},
            {path: 'question', loadChildren: () => import('app/modules/admin/question/question.routes')},
             {path: 'quiz', loadChildren: () => import('app/modules/admin/quiz/quiz.routes')},
            {path: 'user', loadChildren: () => import('app/modules/admin/user/user.routes')},
        ]
    }
];
